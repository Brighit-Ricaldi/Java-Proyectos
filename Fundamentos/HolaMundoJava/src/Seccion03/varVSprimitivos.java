public class varVSprimitivos {
    
    public static void main(String args[]){
    
        var numEntero = 10;
        System.out.println("numEntero = " + numEntero);
        
        var numeroDouble =12.34;
        System.out.println("numeroDouble = " + numeroDouble);
        
        var numeroFloat = 10.98F;
        System.out.println("numeroFloat = " + numeroFloat);
        
        char miCaracter ='0';
        System.out.println("miCaracter = " + miCaracter);
        
        char varChar ='\u0021';
        System.out.println("varChar = " + varChar);
        
        char varCharDecimal = 33;
        System.out.println("varCharDecimal = " + varCharDecimal);
        
        char varCharSimbolo = '!';
        System.out.println("varCharSimbolo = " + varCharSimbolo);
        
        //Ahora utilizaremos CHAR
        
        var varChar2 ='\u0021';
        System.out.println("varChar2 = " + varChar2);
        
        var varCharDecimal3 = (char)33;
        System.out.println("varCharDecimal3 = " + varCharDecimal3);
        
        var varCharSimbolo4 = '!';
        System.out.println("varCharSimbolo4 = " + varCharSimbolo4);
        
        int variableEnteraSimbolo = '!';
        System.out.println("variableEnteraSimbolo = " + variableEnteraSimbolo);
        
        int letra = 'a';
        System.out.println("letra = " + letra);
        
    }
    
}
